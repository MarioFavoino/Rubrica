    private int rigaSelezionataPrecedente = -1;

    private void rubricaTblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_rubricaTblMouseClicked

        int rigaSelezionata = rubricaTbl.getSelectedRow();

        if (rigaSelezionata == rigaSelezionataPrecedente) {
            svuotaCampi();
            rubricaTbl.clearSelection();
            rigaSelezionataPrecedente = -1;

            System.out.println("Tolto");
        } else {
            trasportaDati();
            rigaSelezionataPrecedente = rigaSelezionata;

            System.out.println("Ho cliccato");
        }

    }//GEN-LAST:event_rubricaTblMouseClicked

