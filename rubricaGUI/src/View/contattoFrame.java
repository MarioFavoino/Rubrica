    public void aggiungiContattoTabella(Contatto c) { //Java Swing tutorial 11 - La tabella (1a parte)

        tableModel = (DefaultTableModel) rubricaTbl.getModel();
        tableModel.addRow(c.toArray());

    }

    public void modificaInTabella(Contatto c, int riga) {
        tableModel = (DefaultTableModel) rubricaTbl.getModel();

        tableModel.setValueAt(c.getNome(), riga, 0);
        tableModel.setValueAt(c.getCognome(), riga, 1);
        tableModel.setValueAt(c.getTelefono(), riga, 2);
        tableModel.setValueAt(c.getEmail(), riga, 3);

    }

    private void trasportaDati() {
        nomeTxt.setText(rubricaTbl.getModel().getValueAt(rubricaTbl.getSelectedRow(), 0).toString());
        cognomeTxt.setText(rubricaTbl.getModel().getValueAt(rubricaTbl.getSelectedRow(), 1).toString());
        telefonoTxt.setText(rubricaTbl.getModel().getValueAt(rubricaTbl.getSelectedRow(), 2).toString());
        emailTxt.setText(rubricaTbl.getModel().getValueAt(rubricaTbl.getSelectedRow(), 3).toString());

        nomeTxt.setEnabled(true);
        cognomeTxt.setEnabled(true);
        telefonoTxt.setEnabled(true);
        emailTxt.setEnabled(true);
        eliminaBtn.setEnabled(true);
        modificaBtn.setEnabled(true);

    }

// Funzione che svuota tutti i campi di testo
    private void svuotaCampi() {
        nomeTxt.setText("");
        cognomeTxt.setText("");
        telefonoTxt.setText("");
        emailTxt.setText("");

        nomeTxt.setEnabled(false);
        cognomeTxt.setEnabled(false);
        telefonoTxt.setEnabled(false);
        emailTxt.setEnabled(false);
        eliminaBtn.setEnabled(false);
        modificaBtn.setEnabled(false);
    }

    public void togliElementi() {

        tableModel = (DefaultTableModel) rubricaTbl.getModel();

        tableModel.removeRow(rubricaTbl.getSelectedRow());
        rubricaTbl.repaint();
        rubricaTbl.revalidate();
        rigaSelezionataPrecedente = -1;

        svuotaCampi();

    }

    private Contatto getContattoModificato() {
        Contatto contatto = new Contatto(nomeTxt.getText(), cognomeTxt.getText(), telefonoTxt.getText(), emailTxt.getText());

        return contatto;
    }

