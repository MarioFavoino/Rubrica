    private void modificaBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificaBtnActionPerformed

        Contatto c = getContattoModificato();
        int riga = rubricaTbl.getSelectedRow();

        if (controller.modificaContatto(riga, c)) {
            modificaInTabella(c, riga);
        } else {
            JOptionPane.showMessageDialog(this, "Modifica non consentita", "Modifica", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_modificaBtnActionPerformed

